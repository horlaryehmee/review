	/**
	 * Render social icons widget output on the frontend.
	 *
	 * Written in PHP and used to generate the final HTML.
	 *
	 * @since 1.0.0
	 * @access protected
	 */
	protected function render() {
		$settings = $this->get_settings_for_display();
		
		$class_animation = '';

		if ( ! empty( $settings['hover_animation'] ) ) {
			$class_animation = ' elementor-animation-' . $settings['hover_animation'];
		}

		$listing = \MyListing\Src\Listing::get( get_the_ID() );

		if ( !$listing || !$listing->type ) {
			return;
		}

		if($settings['source'] == 'author'){
			$author = $listing->get_author();
			if ( ! ( $author instanceof \MyListing\Src\User && $author->exists() ) ) {
				return;
			}

			$networks = $author->get_social_links();
		} else{
			$networks = $listing->get_social_networks();
		}
		if ( empty( $networks ) ) {
			return;
		}

		?>
		<div class="elementor-social-icons-wrapper">
			<?php
			foreach ( $networks as $index => $network ) {
				$social = \strtolower($network['key']);

				$link_key = 'link_' . $index;

				$this->add_render_attribute( $link_key, 'class', [
					'elementor-icon',
					'elementor-social-icon',
					'elementor-social-icon-' . $social . $class_animation,
					'elementor-repeater-item-' . $index,
				] );

				$this->add_link_attributes( $link_key, [
					'url' => $network['link'],
					'is_external' => 'true'
				] );

				?>
				<a <?php echo $this->get_render_attribute_string( $link_key ); ?>>
					<span class="elementor-screen-only"><?php echo $network['name'] ?></span>
					<i class="<?php echo esc_attr( $network['icon'] ); ?>"></i>
				</a>
			<?php } ?>
		</div>
		<?php
	}

